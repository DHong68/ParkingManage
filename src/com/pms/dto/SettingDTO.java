package com.pms.dto;

public class SettingDTO {

	int idx;
	int count;
	int dtime;
	int fare;
	int otime;
	int ofare;
	int month_fare;

	public int getIdx() {
		return idx;
	}

	public void setIdx(int idx) {
		this.idx = idx;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public int getDtime() {
		return dtime;
	}

	public void setDtime(int dtime) {
		this.dtime = dtime;
	}

	public int getFare() {
		return fare;
	}

	public void setFare(int fare) {
		this.fare = fare;
	}

	public int getOtime() {
		return otime;
	}

	public void setOtime(int otime) {
		this.otime = otime;
	}

	public int getOfare() {
		return ofare;
	}

	public void setOfare(int ofare) {
		this.ofare = ofare;
	}

	public int getMonth_fare() {
		return month_fare;
	}

	public void setMonth_fare(int month_fare) {
		this.month_fare = month_fare;
	}

}
